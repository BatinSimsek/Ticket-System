     * Show the form for editing the specified resource.
     *
     *
     * @return \Illuminate\Http\RedirectResponse
     */
    public function edit()
    {
        if(Auth::user()){
            $user = User::find(Auth::user()->id);

            if($user){
                return view('users.user.edit')->withUser($user);

            }else{
                return redirect()->back();
            }
        }else{
            return redirect()->back();
        }
    }

